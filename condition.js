//Traffic Light:
//user enter: yellow:slow down
//green:go
//red:stop
let light=prompt("Enter the traffic light color:");
if(light==="yellow"){
    console.log("slow down");
}else if(light==="green"){
    console.log("go");
}else{
    console.log("stop");
}


